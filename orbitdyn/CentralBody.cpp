/*!
85km以下大气密度\n
输入: 高度h(m)\n
输出: 大气密度(kg/m^3)
一般使用函数SA76()调用
*/
double SA76_LOW(double h)
{
	using namespace Atmosphere;
	double g0=9.80665;
	double rr=287.053;
	double r0=6356766.;
	double hp,dhp,tt,a,b,p;
	int k,j=7;

	hp=h/(r0+h)*r0;
	for(k=0;k<=6;k++)
	{
		if(hpi[k]<=hp && hp<hpi[k+1]) j=k;
	}
	dhp=hp-hpi[j];
	tt=tti[j]+eli[j]*dhp;
	if(eli[j]!=0)
	{
		a=1+dhp*eli[j]/tti[j];
		b=-g0/(rr*eli[j]);
		p=pi[j]*pow(a,b);
	}
	else
	{
		a=-g0*dhp/(rr*tti[j]);
		p=pi[j]*exp(a);
	}
	return p/(rr*tt);
}
/*!
85km以上大气密度\n
输入: 高度h(m)\n
输出: 大气密度(kg/m^3)
一般使用函数SA76()调用
*/
double SA76_HIGH(double h)
{
	using namespace Atmosphere;
	double hk,dif,diff,difff,dsr;
	int k,n=24;

	hk=h*.001;
	for(k=0;k<=23;k++)
	{
		if(hk>=feoc[k][0] && hk<feoc[k+1][0]) n=k;
	}
	if(fabs(hk-1001.)<1e-09) n=24;
	dif=hk-feoc[n][0];
	diff=dif*dif;
	difff=diff*dif;
	dsr=feoc[n][1]+feoc[n][2]*dif+feoc[n][3]*diff+feoc[n][4]*difff;
	return exp(log(10.)*dsr);
}
/*!
美国1976标准大气模型\n
根据高度，高度小于85km时调用SA76_LOW()，高度大于85km时调用SA76_HIGH()
输入: 高度h(km)\n
输出: 大气密度(kg/m^3)
*/
double SA76(double h)
{
	if( h > 1000.0 ) return 0; // 1000km以上的大气密度为0
	if(h<85.0)
	{
		if(h<0) h=0.1;
		return SA76_LOW(h*1000);
	}
	else
		return SA76_HIGH(h*1000);
}
